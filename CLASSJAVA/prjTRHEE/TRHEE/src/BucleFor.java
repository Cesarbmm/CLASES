public class BucleFor {
    //metodo: + - + - + - + - + - + -
    public void signoAlterno(){
        System.out.println("FOR: ");
        for(int i=1;i<10;i++){
            if(i%2==0){
                System.out.print("- ");
            }else{
                System.out.print("+ ");
            }
        }
        System.out.println();

    }

}
